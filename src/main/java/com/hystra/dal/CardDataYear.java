package com.hystra.dal;

public class CardDataYear {
    String status;
    String achUnit;
    String targetUnit;
    String unitPerc;
    String achTPValue;
    String targetTPValue;
    String TPValuePerc;

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getAchUnit() {
        return achUnit;
    }

    public void setAchUnit(String achUnit) {
        this.achUnit = achUnit;
    }

    public String getTargetUnit() {
        return targetUnit;
    }

    public void setTargetUnit(String targetUnit) {
        this.targetUnit = targetUnit;
    }

    public String getUnitPerc() {
        return unitPerc;
    }

    public void setUnitPerc(String unitPerc) {
        this.unitPerc = unitPerc;
    }

    public String getAchTPValue() {
        return achTPValue;
    }

    public void setAchTPValue(String achTPValue) {
        this.achTPValue = achTPValue;
    }

    public String getTargetTPValue() {
        return targetTPValue;
    }

    public void setTargetTPValue(String targetTPValue) {
        this.targetTPValue = targetTPValue;
    }

    public String getTPValuePerc() {
        return TPValuePerc;
    }

    public void setTPValuePerc(String TPValuePerc) {
        this.TPValuePerc = TPValuePerc;
    }
}
